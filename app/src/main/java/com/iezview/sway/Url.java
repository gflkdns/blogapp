public class Url {
    /**
     * sway3d : http://sway-3d.com/sway
     * model : http://sway-3d.com
     * mus3d : http://sway-3d.com/api
     */

    private String sway3d;
    private String model;
    private String mus3d;

    public String getSway3d() {
        return sway3d;
    }

    public void setSway3d(String sway3d) {
        this.sway3d = sway3d;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getMus3d() {
        return mus3d;
    }

    public void setMus3d(String mus3d) {
        this.mus3d = mus3d;
    }
}
